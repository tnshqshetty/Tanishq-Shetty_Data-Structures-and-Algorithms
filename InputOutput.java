 import java.util.*;

public class InputOutput {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        System.out.println(n);
        sc.close();
    }
}

/*Complexity Analysis:
Time Complexity: O(1)  as the program carries out a single constant-time task, namely printing a fixed, predetermined number ,time and operations is constant

Space Complexity: O(1)  because no extra space was used.*/


